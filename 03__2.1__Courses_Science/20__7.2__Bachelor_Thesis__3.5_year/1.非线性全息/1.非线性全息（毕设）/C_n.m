function n = C_n(lam,T)
%计算铌酸锂色散折射率，针对e光入射（偏振方向和光轴平行）5% MgO 
a1e=5.756;
a2e=0.0983;
a3e=0.2020;
a4e=189.32;
a5e=12.52;
a6e=1.32*10^(-2);
b1e=2.860*10^(-6);
b2e=4.700*10^(-8);
b3e=6.113*10^(-8);
b4e=1.516*10^(-4);
F=(T-24.5)*(T+570.82);
n=sqrt(a1e+b1e*F+(a2e+b2e*F)./(lam.^2-(a3e+b3e*F)^2)+(a4e+b4e*F)./(lam.^2-a5e^2)-a6e*lam.^2);
end

